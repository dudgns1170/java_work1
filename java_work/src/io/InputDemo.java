package io;

public class InputDemo {

	public static void main(String[] args) { // args 란 args 라는 변수를 정의한 것임 다른 것이 와도 상관 없음.
											 // 문자열 데이터 타입을 담을 수 있는 배열을 의미함
		System.out.println(args.length);	 // String[] args 가 소괄호에 있는 이유 == 이 친구가 메인 함수의 매개변수라는 뜻
											 // void 는 무엇인가? 메인 함수의 출력값이 없다는 뜻
	}										 // args.length 는 배열에 담긴 원소의 개수가 아니라 몇개의 배열을 담을 수 있는지

}

